package licenses

import (
	"context"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"regexp"
	"time"

	"github.com/golang/glog"
	"github.com/google/go-licenses/v2/gocli"
	"github.com/google/go-licenses/v2/third_party/go/pkgsite/source"
)

var licenseRegexp = regexp.MustCompile(`^(?i)(LICEN(S|C)E|COPYING|README|NOTICE).*$`)

// License is an open source software license.
type License struct {
	ID   string // SPDX ID. https://spdx.org/licenses.
	Path string // Relative path in the module.
	URL  string // Optional, license file URL on internet.
	Type Type   // Type of the software license.
}

// Module is a go module with license info.
// There can be multiple licenses, because of several different reasons.
// Some common reasons include:
// * Modules may vendor internal packages of other modules. The vendored packages have a different license.
// * Modules may be rewritten from source code of a different langugage. Both
// the original license and the new license applies to the code.
// * Extra files may be false-positives of the license classifier. So you should
// verify the results generated by this tool.
type Module struct {
	gocli.Module
	Licenses []License
}

func (m *Module) String() string {
	if m == nil {
		return ""
	}
	return m.Path
}

// Error when we find an empty DIR
var ErrorEmptyDir = fmt.Errorf("dir is empty")

// ScanOptions configures behavior of Scan.
type ScanOptions struct {
	// By default, Scan does not get remote URLs of licenses.
	// Turning on this config gets URLs.
	// Failing to find a URL does not result in an error, the URL field will
	// be an empty string instead and a warning message will be logged.
	GetURL bool
}

// Scan scans a module for licenses.
func Scan(ctx context.Context, m gocli.Module, classifier Classifier, opts ScanOptions) (res Module, err error) {
	defer func() {
		if err != nil {
			err = fmt.Errorf("scanning licenses for module %q: %w", m.Path, err)
		}
	}()
	res.Module = m
	if m.Dir == "" {
		return res, ErrorEmptyDir
	}
	var remote *source.Info
	if opts.GetURL {
		client := source.NewClient(time.Second * 20)
		ver := m.Version
		if ver == "" {
			// This always happens for the module in development.
			ver = "master"
			glog.Warningf("module %s has empty version, defaults to master. The license URL may be incorrect. Please verify!", m.Path)
		}
		remote, err = source.ModuleInfo(ctx, client, m.Path, ver)
		if err != nil {
			glog.Warningf("finding module info for %s: %s", m.Path, err)
			// Do not exit early, because URL is considered to be an optional field.
			remote = nil
		}
	}
	res.Licenses = make([]License, 0)
	err = filepath.Walk(m.Dir, func(path string, info fs.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.Mode()&os.ModeSymlink != 0 {
			// skip symbolic links
			return nil
		}
		if info.IsDir() {
			if ignoredDir[info.Name()] {
				return filepath.SkipDir
			}
			return nil
		}
		if !licenseRegexp.MatchString(info.Name()) {
			// Skip file names that does not look like a license file.
			return nil
		}
		licenseID, licenseType, err := classifier.Identify(path)
		if err != nil {
			// It's expected for files without license text in it.
			return nil
		}
		relativePath, err := filepath.Rel(m.Dir, path)
		if err != nil {
			return err
		}
		res.Licenses = append(res.Licenses, License{
			ID:   licenseID,
			Path: relativePath,
			URL:  remote.FileURL(relativePath), // remote.FileURL returns empty string when remote is nil.
			Type: licenseType,
		})
		return nil
	})
	if len(res.Licenses) == 0 {
		return res, fmt.Errorf("license not found")
	}
	return res, err
}

var ignoredDir map[string]bool = make(map[string]bool)

func init() {
	ignoredDir[".git"] = true
	ignoredDir["node_modules"] = true
	ignoredDir["testdata"] = true
}
